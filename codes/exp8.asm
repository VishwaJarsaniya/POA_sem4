DATA SEGMENT
    S1 DB 10H,5H,11H,3H,6H
    LEN DB 05H
    MIN DB ?
    MAX DB ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA     

START:
MOV AX,DATA
MOV DS,AX
LEA SI,S1
MOV AL,[SI]
MOV MAX,AL
MOV MIN,AL
MOV CL,LEN
INC SI

REPEAT1:
MOV AL,[SI]  

CMP AL,MIN
JL UPDATE_MIN

CMP AL,MAX
JG UPDATE_MAX  

JMP CONTINUE_LOOP

UPDATE_MIN:
MOV MIN,AL
JMP CONTINUE_LOOP  

UPDATE_MAX:
MOV MAX,AL
JMP CONTINUE_LOOP

CONTINUE_LOOP:
INC SI
DEC CX
JNZ REPEAT1

CODE ENDS
END START